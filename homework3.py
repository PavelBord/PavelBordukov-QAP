# 5.Найти произведение положительных, сумму и количество отрицательных
# из 10 введенных целых значений. (Функция ввода с клавиатуры - input())

p, s, c = 1, 0, 0

for _ in range(10):
    n = int(input())
    if n > 0:
        p *= n
    if n < 0:
        s += n
        c += 1
print(p, s, c)

# 6.Дано число N. Найти произведение всех чисел от 0 до N

n = int(input("Введите N: "))
result = 1
for i in range(1, n + 1):
    result *= i
print(f"Результат: {result}")

# 7. Поле засеяли цветами двух сортов на площади S1 и S2. Каждый год
# площадь цветов первого сорта увеличивается вдвое, а площадь второго сорта
# увеличивается втрое. Через сколько лет площадь первых сортов будет
# составлять меньше 10% от площади вторых сортов

s_1 = float(input("Площадь первого сорта: "))
s_2 = float(input("Площадь второго сорта: "))

years = 0

while s_1 >= 0.1 * s_2:
    s_1 *= 2
    s_2 *= 3
    years += 1
print(f"Через сколько лет:{years} ")

# 8.Напишите алгоритм для определения, является ли число n счастливым.
# Счастливое число определяется следующим процессом: Начиная с любого положительного целого числа,
# замените число суммой квадратов его цифр. Повторяйте процесс до тех пор, пока число не станет равным 1
# (на этом значении оно и останется), или пока не замкнется в бесконечном цикле,
# не включающем 1. Числа, для которых этот процесс заканчивается на 1, являются счастливыми.
# Верните true, если n — счастливое число, и false, если нет.

# Example 1:
# Input: n = 19
# Output: true

# Explanation:
# 1**2 + 9**2 = 82
# 8**2 + 2**2 = 68
# 6**2 + 8**2 = 100
# 1**2 + 0**2 + 0**2 = 1

n = int(input("Введите число: "))
seen = set()
while n != 1 and n not in seen:
    seen.add(n)
n = sum(int(d) ** 2 for d in str(n))
if n == 1:
    print("True")
else:
    print("False")
